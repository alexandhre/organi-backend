        <p>
            Entre outros princípios e instrumentos introduzidos pela Política Nacional de Resíduos Sólidos (PNRS), Lei nº 12.305, de 2 de agosto de 2010, e seu regulamento, Decreto Nº 7.404 de 23 de dezembro de 2010,
            destacam-se a responsabilidade compartilhada pelo ciclo de vida dos produtos e a logística reversa.</p>
        <p>
            Nos termos da PNRS, a responsabilidade compartilhada pelo ciclo de vida dos produtos é o "conjunto de atribuições individualizadas e encadeadas dos fabricantes, importadores, distribuidores e comerciantes,
            dos consumidores e dos titulares dos serviços públicos de limpeza urbana e de manejo dos resíduos sólidos, para minimizar o volume de resíduos sólidos e rejeitos gerados, bem como para reduzir os impactos causados à saúde humana
            e à qualidade ambiental decorrentes do ciclo de vida dos produtos, nos termos desta Lei."</p>
        <p>
            A logística reversa é um dos instrumentos para aplicação da responsabilidade compartilhado pelo ciclo de vida dos produtos. A PNRS define a logística reversa como um "instrumento de desenvolvimento econômico e social caracterizado por
            um conjunto de ações, procedimentos e meios destinados a viabilizar a coleta e a restituição dos resíduos sólidos ao setor empresarial, para reaproveitamento, em seu ciclo ou em outros ciclos produtivos, ou outra destinação final ambientalmente adequada.”</p>
        <p>
            De acordo com Decreto nº 7.404/2010 os sistemas de logística reversa serão implementados e operacionalizados por meio dos seguintes instrumentos:</p>

        <p> Regulamento expedido pelo Poder Público</p>
        <p> Neste caso a logística reversa poderá ser implantada diretamente por regulamento, veiculado por decreto editado pelo Poder Executivo.</p>
        <p> Antes da edição do regulamento, o Comitê Orientador deverá avaliar a viabilidade técnica e econômica da logística reversa. Os sistemas de logística reversa estabelecidos diretamente por decreto deverão ainda ser precedidos de consulta pública.</p>
        <p>  Acordos Setoriais </p>
        <p>Os acordos setoriais são atos de natureza contratual, firmados entre o Poder Público e os fabricantes, importadores, distribuidores ou comerciantes, visando a implantação da responsabilidade compartilhada pelo ciclo de vida dos produtos.</p>
        <p> O processo de implantação da logística reversa por meio de um acordo setorial poderá ser iniciado pelo Poder Público ou pelos fabricantes, importadores, distribuidores ou comerciantes dos produtos e embalagens referidos no art. 18 do Decreto nº 7.404/2010.
            Os procedimentos para implantação da logística reversa por meio de um acordo setorial estão listados na subseção I da seção II do Capítulo III do Decreto nº 7.404/2010.</p>
        <p> Termos de Compromisso</p>
        <p> O Poder Público poderá celebrar termos de compromisso com fabricantes, importadores, distribuidores ou comerciantes visando o estabelecimento de sistema de logística reversa:   </p>
        <p>I - nas hipóteses em que não houver, em uma mesma área de abrangência, acordo setorial ou regulamento específico, consoante o estabelecido no Decreto nº 7.404/2010; ou  </p>
        <p> II - para a fixação de compromissos e metas mais exigentes que o previsto em acordo setorial ou regulamento.</p>
        <p>Os termos de compromisso terão eficácia a partir de sua homologação pelo órgão ambiental competente do SISNAMA, conforme sua abrangência territorial.</p>
